	SUBROUTINE SUN (BETA,RP,T)
C
C	SUN CODE
C
C
	IMPLICIT REAL*8 (A-H,L,O-Z)
	IMPLICIT INTEGER*4 (I-K,M-N)
	COMMON /FMCMMN/ PLON,U,V,W,A1,A2,A3,A4,A5,A6,A7,A8,A9,
	1   A10,A11,A12,A13,A14,A15,A16,A17,A18,A19,A20,A21,A22,A23,
	1   A24,A25,A26,A27,A28,A29,A30,A31,A32,A33
C
C
	PLON=6910*DSIN(A8)+72*DSIN(2*A8)-17*T*DSIN(A8)-7*DCOS(A8-A19)
	1   +6*DSIN(A1-A7)+5*DSIN(4*A8-8*A16+3*A19)-5*DCOS(2*A8-2*A13)
	2   -4*DSIN(A8-A13)+4*DCOS(4*A8-8*A16+3*A19)+3*DSIN(2*A8-2*A13)
	3   -3*DSIN(A19)-3*DSIN(2*A8-2*A19)
C	DONE
	BETA=0.0
C	DONE
	RP=1.00014D0-0.01675*DCOS(A8)-0.00014*DCOS(2*A8)
C	DONE
	RETURN
	END
