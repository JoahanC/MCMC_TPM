	SUBROUTINE C50TOG(C50,G)
C
C	CONVERTS VECTOR C FROM CELESTIAL COORDINATES (1950.0)
C	TO GALACTIC COORDINATE VECTOR G
C
	DIMENSION G(3),C50(3),R(3,3)
	DATA R/-.0669888,.4927285,-.8676008,
	1       -.8727558,-.450347,-.1883746,
	2       -.4835389,.7445847,.4601998/
	CALL MAV(R,C50,G)
	RETURN
	ENTRY GTOC50(G,C50)
C
C	CONVERTS GALACTIC IN G TO CELESTIAL IN C
C
	CALL VMAT(R,G,C50)
	RETURN
	END
