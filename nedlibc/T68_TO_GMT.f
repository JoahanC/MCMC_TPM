 	SUBROUTINE T68_TO_GMT(T68,GMT)
C
C	CONVERT T68 REAL*8 SECONDS SINCE 5/24/68 (JD 2440000.5)
C	INTO GMT ASCII FORMAT 'YYDDDHHMMSSFFF'
C
	REAL*8 T68
	CHARACTER*14 GMT
	INTEGER IY,IM,ID,IH,MINUTE,I367,JD,JD11,IS,I
	REAL*4 SECOND
	DATA I367/367/
	CALL T68_TO_UTC(T68,IY,IM,ID,IH,MINUTE,SECOND)
	IF (IH.GE.24) THEN
	  IH = IH-24
	  ID = ID+1
	ENDIF
	JD=-7*(IY+(IM+9)/12)/4-3*((IY+(IM-9)/7)/100+1)/4+275*IM/9+ID
	JD11=-7*IY/4-3*((IY-1)/100+1)/4+30
	ID = JD-JD11
	IY = MOD(IY,100)
	IS = 1000.0*SECOND
	WRITE (GMT,FMT='(I2,I3,2I2,I5)') IY,ID,IH,MINUTE,IS
	DO I=1,14
	  IF (GMT(I:I).EQ.' ') GMT(I:I) = '0'
	ENDDO
	RETURN
	END
