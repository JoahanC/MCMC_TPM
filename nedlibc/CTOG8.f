	SUBROUTINE CTOG8(C2000,G)
C
C	CONVERTS VECTOR C FROM CELESTIAL COORDINATES (2000.0)
C	TO GALACTIC COORDINATE VECTOR G
C
	REAL*8 G(3),C2000(3),R(3,3)
C	see http://adsabs.harvard.edu/abs/1989A&A...218..325M
C	Murray, C. 1989, ApJ, 218, 325
C	"The transformation of coordinates between the systems of B1950.0 
C	and J2000.0, and the principal galactic axes referred to J2000.0"
C
	DATA R/ -0.0548755604D0,  0.4941094279D0, -0.8676661490D0,
	1       -0.8734370902D0, -0.4448296300D0, -0.1980763734D0,
 	2       -0.4838350155D0,  0.7469822445,  0.4559837762D0/
C
	G(1) = R(1,1)*C2000(1)+R(1,2)*C2000(2)+R(1,3)*C2000(3)
	G(2) = R(2,1)*C2000(1)+R(2,2)*C2000(2)+R(2,3)*C2000(3)
	G(3) = R(3,1)*C2000(1)+R(3,2)*C2000(2)+R(3,3)*C2000(3)
	RETURN
	ENTRY GTOC8(G,C2000)
C
C	CONVERTS GALACTIC IN G TO CELESTIAL IN C
C
	C2000(1) = R(1,1)*G(1)+R(2,1)*G(2)+R(3,1)*G(3)
	C2000(2) = R(1,2)*G(1)+R(2,2)*G(2)+R(3,2)*G(3)
	C2000(3) = R(1,3)*G(1)+R(2,2)*G(2)+R(3,3)*G(3)
	RETURN
	END
