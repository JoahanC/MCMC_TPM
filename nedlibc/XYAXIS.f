	SUBROUTINE XYAXIS(NFACE,XI,ETA,C)
C
C	CONVERTS FACE NUMBER NFACE (0-5) AND TANGENT PLANE
C	CO-ORDINATES XI, ETA (-1. - +1.) INTO A UNIT VECTOR C
C
	REAL*4 C(3),XI,ETA
	GO TO (200,210,220,230,240,250), NFACE+1
200	  C(3)=1.
	  C(1)=-ETA				!Transform face to sphere
	  C(2)=XI
	  GO TO 300
210	  C(1)=1.
	  C(3)=ETA
	  C(2)=XI
	  GO TO 300
220	  C(2)=1.
	  C(3)=ETA
	  C(1)=-XI
	  GO TO 300
230	  C(1)=-1.
	  C(3)=ETA
	  C(2)=-XI
	  GO TO 300
240	  C(2)=-1.
	  C(3)=ETA
	  C(1)=XI
	  GO TO 300
250	  C(3)=-1.
	  C(1)=ETA
	  C(2)=XI
	  GO TO 300
C
300	CALL NORM(C)
	RETURN
	END
