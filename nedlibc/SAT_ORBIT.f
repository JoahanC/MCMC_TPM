	SUBROUTINE SAT_ORBIT(ABUF1,ABUF2,NT,TIN,XYZ)
	INTEGER NT		! number of times
	REAL*8 TIN(NT)		! times for predictions
	REAL*8 TS(14400)	! times since TOSC in minutes
	REAL*4 V(3,14400)
	REAL*4 XYZ(3,NT)	! output in geocentric km, current RA+DEC
	CHARACTER*14 GMT
	CHARACTER*80 ABUF1,ABUF2	! two line orbital elements
	REAL*8 TOSC,EPOCH
	REAL*8 GMT2T68
	READ (ABUF1,FMT='(18X,A,D9.8)') GMT(1:5),EPOCH
	GMT(6:14) = '000000000'
	TOSC = GMT2T68(GMT) + 864.D2*EPOCH
	DO I=1,NT
	  TS(I) = (TIN(I)-TOSC)/6.D1
	ENDDO
	CALL SATPOS(ABUF1,ABUF2,NT,TS,XYZ,V)
	RETURN
	END
