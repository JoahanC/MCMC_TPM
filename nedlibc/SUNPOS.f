	SUBROUTINE SUNPOS(T,SCEL)
C
C	NEW VERSION OF SUNPOS THAT USES EPHEM ROUTINES
C	30-AUG-83
C
C	INPUT T REAL*8 SECONDS SINCE 5/24/68
C
C	OUTPUT SCEL, REAL*4 SCEL(3), UNIT VECTOR POINTING AT SUN
C	in celestial J2000 system
C
	REAL*8 T,T1,T2
	REAL*4 SCEL(3)
	REAL*4 S1(3) /0.,0.,0./
	REAL*4 S2(3) /0.,0.,0./
	DATA T1,T2/1.D20,-1.D20/
	SAVE
	IF (T1.LE.T .AND. T.LE.T2) GO TO 100
	   T1=T-100.D0
	   CALL EPHEM(-1,T1,S1)
	   CALL NORM(S1)
	   T2=T1+3800.D0
	   CALL EPHEM(-1,T2,S2)
	   CALL NORM(S2)
100	X=(T-T1)/(T2-T1)
	Y=1.-X
	DO 150 I=1,3
150	SCEL(I)=X*S2(I)+Y*S1(I)
	RETURN
	END
