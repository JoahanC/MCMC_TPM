      REAL*4 FUNCTION ZODI(ANU)
      REAL*4 ANU
C
C     RETURNS SKY BACKGROUND IN ERG/CM**2/SR/SEC/WAVENUMBER FOR
C     ANU IN WAVENUMBER (1/CM)
C
C     REVISED: 8-Nov-1994 by Ned Wright
C           to FIT dirbe fluxes at for ecliptic pole and 
C           the Wright etal (1991) fit to the FIRAS data with G(l,b)=0.05
C
C     SCATTERED SUNLIGHT
      DATA TS,WS/5500.,4.756E-14/
C     THERMAL ZODIACAL LIGHT
      DATA E1,TH,TC,AC/2.761E-7,268.126,157.037,20.878/
C     GALACTIC DUST
      DATA TAU1,TGAL,GC,TCOLD/0.122E-7,20.4,6.7,4.77/
      REAL LAMBDA,INTENS
      LAMBDA=10000./ANU
      INTENS=WS*(LAMBDA/2.2)**0.351538*PLANCK(ANU,TS)
     1  +(E1/LAMBDA)*(PLANCK(ANU,TH)+AC*PLANCK(ANU,TC))
     2  +TAU1*ANU**2*(PLANCK(ANU,TGAL)+GC*PLANCK(ANU,TCOLD))
     3  +2.484E-10/ANU**0.75
     4  +PLANCK(ANU,2.725)
      ZODI=INTENS
      RETURN
      END
