	FUNCTION PLANCK(ANU,T)
C
C	GIVES ERGS/CM**2/SR/SEC/WAVENUMBER
C	T IN KELVIN
C	ANU IN 1/CM
C
	REAL*4 THCC,HCK
	PARAMETER (THCC=1.19104272E-5,HCK=1.4387752)
	IF (T.GT.0.) GO TO 10
5	PLANCK = 0.
	RETURN
10	X = HCK*ANU/T
	IF (X.GT.50.) GO TO 5
	IF (X.GT..01) GO TO 15
	D = X*(1.+X*(.5+X/6.))
	GO TO 20
15	D = EXP(X)-1.
20	PLANCK = THCC*ANU**3/D
	RETURN
	END
