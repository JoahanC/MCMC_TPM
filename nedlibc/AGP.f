	REAL FUNCTION AGP(X)
	IMPLICIT NONE
	REAL X
C
C	CONVERTS PROBABILITY X INTO NUMBER OF SIGMA
C
	REAL T, XP
C
C	default to +/- 6 sigma for out of range inputs
C
	IF (X.LE.0.) THEN
	  AGP = -6.0
	  RETURN
	ENDIF
	IF (X.GE.1.) THEN
	  AGP = 6.0
	  RETURN
	ENDIF
C
	T = SQRT(-2.0*ALOG(0.5-ABS(X-0.5)))
	XP = T -(2.515517+T*(0.802853+T*0.010328))/
	1	(1.0+T*(1.432788+T*(0.189269+T*0.001308)))
	IF (X.LT.0.5) XP = -XP
	AGP = XP
	RETURN
	END
