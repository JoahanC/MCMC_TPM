      REAL*8 FUNCTION ACTAN(SINX,COSX)
      IMPLICIT REAL*8 (A-H,O-Z)
	PARAMETER (DE2RA=0.0174532925199433, PI=3.1415926535897900)
	PARAMETER (PIO2=PI/2,TWOPI=2*PI,X3PIO2=1.5D0*PI)
      ACTAN=0.
      IF (COSX.EQ.0.  ) GO TO 5
      IF (COSX.GT.0.  ) GO TO 1
      ACTAN=PI
      GO TO 7
    1 IF (SINX.EQ.0.  ) GO TO 8
      IF (SINX.GT.0.  ) GO TO 7
      ACTAN=TWOPI
      GO TO 7
    5 IF (SINX.EQ.0.  ) GO TO 8
       IF (SINX.GT.0.  ) GO TO 6
      ACTAN=X3PIO2
      GO TO 8
    6 ACTAN=PIO2
      GO TO 8
    7 TEMP=SINX/COSX
      ACTAN=ACTAN+ATAN(TEMP)
    8 RETURN
      END
