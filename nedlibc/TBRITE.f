	FUNCTION TBRITE(ANU,PLANCK)
	REAL*4 THCC,HCK
	PARAMETER (THCC=1.19104272E-5,HCK=1.4387752)
	IF (PLANCK.GT.0.) GO TO 10
5	TBRITE = 0.
	RETURN
10	IF (ANU.LE.0.) GO TO 5
	D = THCC*ANU**3/PLANCK
	IF (D.GT..01) GO TO 15
	X = D*(1.-D*(.5-D/3.))
	GO TO 20
15	X = ALOG(1.+D)
20	TBRITE = HCK*ANU/X
	RETURN
	END
