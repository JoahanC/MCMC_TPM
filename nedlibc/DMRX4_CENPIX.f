	SUBROUTINE DMRX4_CENPIX(NDMR,C)
C
C	input NPIX in 0..24575
C	output C unit vector
C	made from DMR_CENPIX 4-SEP-91
C
	INTEGER NDMR
	REAL*4 C(3)				!Output unit vector
C
	INTEGER*2 IX,IY
	COMMON /COBE_SPRCNPX/ IX(0:1023),IY(0:1023)
	IF(IX(1023).NE.31 .OR. IY(1023).NE.31) CALL COBE_SPRCNPX_SET
C
	IF (NDMR.LT.0 .OR. NDMR.GT.24575) THEN
	  WRITE (*,*) 'BAD INPUT IN DMRX4_CENPIX'
	  STOP
	ENDIF
	NFACE=NDMR/4096
	M=MOD(NDMR,4096)
	MM=M/1024
	M=MOD(M,1024)
	JX=32*IX(MM)+IX(M)
	JY=32*IY(MM)+IY(M)
C
	X=(JX-31.5)/32.				!Database coordinates.  Range
	Y=(JY-31.5)/32.				! -1 -> 1 covers the square face
	CALL FORWARD_CUBE(X,Y,XI,ETA)
	CALL XYAXIS(NFACE,XI,ETA,C)
	RETURN
	END
