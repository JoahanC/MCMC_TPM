	SUBROUTINE EDGCHK(NFACE,IX,IY,MAX,MFACE,JX,JY)
C
C	CHECK FOR IX, IY BEING OVER THE EDGE OF A FACE
C	RETURNS CORRECT FACE, IX, IY IN MFACE,JX,JY
C
	IF(IX.LT.0) THEN
	  GO TO (10,20,30,30,30,60), NFACE+1
10	    MFACE=4
	    JY=IX+MAX
	    JX=MAX-1-IY
	    RETURN
20	    MFACE=4
	    GO TO 35
30	    MFACE=NFACE-1
35	    JX=MAX+IX
	    JY=IY
	    RETURN
60	    MFACE=4
	    JX=IY
	    JY=-IX-1
	    RETURN
	ENDIF
	IF(IX.GE.MAX) THEN
	  GO TO (110,120,120,120,150,160), NFACE+1
110	    MFACE=2
	    JX=IY
	    JY=2*MAX-1-IX
	    RETURN
120	    MFACE=NFACE+1
125	    JY=IY
	    JX=IX-MAX
	    RETURN
150	    MFACE=1
	    GO TO 125
160	    MFACE=2
	    JX=MAX-1-IY
	    JY=IX-MAX
	    RETURN
	ENDIF
	IF(IY.LT.0) THEN
	  GO TO (210,220,230,240,250,260), NFACE+1
210	    MFACE=1
	    JY=IY+MAX
	    JX=IX
	    RETURN
220	    MFACE=5
	    JY=IY+MAX
	    JX=IX
	    RETURN
230	    MFACE=5
	    JX=IY+MAX
	    JY=MAX-1-IX
	    RETURN
240	    MFACE=5
	    JX=MAX-1-IX
	    JY=-IY-1
	    RETURN
250	    MFACE=5
	    JX=-IY-1
	    JY=IX
	    RETURN
260	    MFACE=3
	    JX=MAX-1-IX
	    JY=-IY-1
	    RETURN
	ENDIF
	IF(IY.GE.MAX) THEN
	  GO TO (310,320,330,340,350,360) NFACE+1
310	    MFACE=3
	    JX=MAX-1-IX
	    JY=2*MAX-1-IY
	    RETURN
320	    MFACE=0
	    JY=IY-MAX
	    JX=IX
	    RETURN
330	    MFACE=0
	    JX=2*MAX-1-IY
	    JY=IX
	    RETURN
340	    MFACE=0
	    JX=MAX-1-IX
	    JY=2*MAX-1-IY
	    RETURN
350	    MFACE=0
	    JX=IY-MAX
	    JY=MAX-1-IX
	    RETURN
360	    MFACE=1
	    JX=IX
	    JY=IY-MAX
	    RETURN
	ENDIF
	MFACE=NFACE
	JX=IX
	JY=IY
	RETURN
	END
