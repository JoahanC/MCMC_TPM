	FUNCTION GST(T)
C
C	BY NED WRIGHT 5-AUG-81
C	MODIFIED 7-MAY-1985 FOR NEW ZERO
C	MODIFIED 20-JAN-1988 TO AVOID NEGATIVE OUTPUTS FROM DMOD FOR TIMES
C		BEFORE 1-JAN-1987
C
C	COMPUTES GREENWICH SIDEREAL TIME IN RADIANS FROM
C	T, WHICH IS REAL*8 IAT SECONDS FROM 5/24/68
C
	REAL*8 T,RATE,GST0UT,T1187
	DATA RATE,GST0UT/1.002737909316D0,24027.319D0/
	DATA T1187 /587174416.52D0/
	DATA TWOPI/6.283185307/
	DATA IENT/0/
	GST=TWOPI*DMOD((RATE*(T-T1187)+GST0UT)/864.D2,1.D0)
	IF (GST.LT.0.) GST = GST + TWOPI
	RETURN
	END
