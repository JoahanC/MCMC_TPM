	SUBROUTINE ELECTRONS(T,NE,E,DNDE)
C
C	GIVES ELECTRON FLUX AT COBE
C	BY NED WRIGHT 30-MAR-83
C
C	INPUT:	T IS REAL*8 SECONDS SINCE 5/24/68
C		NE IS NUMBER OF ENERGIES
C		E IS A REAL*4 ARRAY OF ENERGIES IN MEV
C	OUTPUT:	DNDE IS A REAL*4 ARRAY OF DIFFERENTIAL SPECTRUM
C
	REAL*8 T
	DIMENSION E(NE),DNDE(NE)
	DIMENSION PEAKS(3,3),R(3)
C
C	PEAKS CONTAINS POSITIONS OF THREE GAUSSIANS THAT DEFINE THE
C		SOUTH ATLANTIC ANOMALY
	DATA PEAKS/ .3830,-.8214,-.4226,
	1	    .9830,-.1294,-.1305,
	2	    .7789,-.5421,-.3154/
C
	REAL*4 NHORN(3),SHORN(3),NRAD
	DATA NHORN,NRAD/.0449,-.1677,.9848,  .3039/
	DATA SHORN,SRAD/-.0951,.2039,-.9744, .2180/
	PARAMETER (PI=3.1415927,TWOPI=6.2831853,DTR=57.29578)
C
	CALL TERRESTIAL(T,R)
	CALL NORM(R)
C
C	COMPUTE RATE WITH E GT 0.5 MEV
	E50=100000.*EXP(-AMIN1(70.,444.*(DVS(R,NHORN)-NRAD)**2))
	E50=100000.*EXP(-AMIN1(70.,444.*(DVS(R,SHORN)-SRAD)**2))+E50
	DO 20 I=1,3
20	E50=100000.*EXP(-8.5934*DVS(R,PEAKS(1,I)))+E50
C
C	ASSUME 1/E**2 SPECTRUM SO E50*0.5/E**2
	DO 40 I=1,NE
40	DNDE(I)=0.5*E50/E(I)**2
	RETURN
	END
