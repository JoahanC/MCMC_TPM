	SUBROUTINE LATLONG(T,LAT,LONG)
C
C	FOR INPUT TIME T, REAL*8 SECONDS SINCE 5/24/68
C	RETURNS REAL*4 LAT AND LONG OF SUB-SATELLITE POINT IN DEGREES
C
	REAL*8 T
	REAL*4 LAT,LONG
C
	REAL*4 R(3)
	PARAMETER (PI=3.1415927,TWOPI=6.2831853,DTR=57.29578)
C
	CALL TERRESTIAL(T,R)
	RR=SQRT(R(1)**2+R(2)**2)
	LAT=DTR*ATAN2(R(3),RR)
	LONG=DTR*ATAN2(R(2),R(1))
	RETURN
	END
